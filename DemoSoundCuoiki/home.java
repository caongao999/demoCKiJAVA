    public static void sound(String location) {
        try {
            File musicPath = new File(location); // Use the provided location
            if (musicPath.exists()) {
                AudioInputStream audioIn = AudioSystem.getAudioInputStream(musicPath);
                Clip clip = AudioSystem.getClip();
                clip.open(audioIn);
                clip.start();
            } else {
                System.out.println("File not found: " );
            }
        } catch (UnsupportedAudioFileException e) {
            System.out.println("Unsupported audio file format");
        } catch (LineUnavailableException e) {
            System.out.println("Line unavailable: " + e.getMessage());
        } catch (IOException e) {
            System.out.println("IOException: " + e.getMessage());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

